from typing import Optional, List
from collections import defaultdict


class Solution1:
    def capitalizeTitle(self, title: str) -> str:
        arr = title.split(" ")
        for i, c in enumerate(arr):
            if len(c) <= 2:
                arr[i] = c.lower()
            else:
                s = c[0].upper() + c[1:].lower()
                arr[i] = s
        return " ".join(arr)


class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution2:
    def pairSum(self, head: Optional[ListNode]) -> int:
        pre = head
        arr = []
        n = 0
        while pre:
            arr.append(pre.val)
            pre = pre.next
            n += 1

        limit = n // 2
        ans = 0
        for i in range(limit):
            ans = max(ans, arr[i] + arr[n - 1 - i])

        return ans


class Solution3:
    def longestPalindrome(self, words: List[str]) -> int:
        sameW = []
        cnt = 0
        d = defaultdict(int)

        for w in words:
            if w[0] == w[1]:
                sameW.append(w)
            if w not in d and w[::-1] not in d:
                d[w] += 1
            elif w[0] == w[1]:
                if d[w] != 0:
                    cnt += 1
                if d[w] > 0:
                    d[w] -= 1
                elif d[w] < 0:
                    d[w] += 1
                else:
                    d[w] += 1
            elif w in d:
                if d[w] < 0:
                    cnt += 1
                d[w] += 1
            elif w[::-1] in d:
                reverseW = w[::-1]
                if d[reverseW] > 0:
                    cnt += 1
                d[reverseW] -= 1
        ans = cnt * 4
        for s in sameW:
            if d[s] != 0:
                ans += 2
                break
        return ans


class Solution4:
    def possibleToStamp1(self, grid: List[List[int]], stampHeight: int, stampWidth: int) -> bool:
        m, n = len(grid), len(grid[0])

        for i in range(m):
            for j in range(n):
                if grid[i][j] == 1:
                    continue
                if i + stampHeight - 1 >= m or j + stampWidth - 1 >= n:
                    continue
                isValid = True
                for g in range(i, i + stampHeight):
                    for k in range(j, j + stampWidth):
                        if grid[g][k] == 1:
                            isValid = False
                            break
                if grid[i][j] == 0 and not isValid:
                    return False
                if isValid:
                    for g in range(i, i + stampHeight):
                        for k in range(j, j + stampWidth):
                            grid[g][k] = 2
        for i in range(m):
            for j in range(n):
                if grid[i][j] == 0:
                    return False
        return True

    def possibleToStamp(self, grid: List[List[int]], stampHeight: int, stampWidth: int) -> bool:
        m, n = len(grid), len(grid[0])

        P = [[0] * (n + 1) for _ in range(m + 1)]
        for i in range(1, m + 1):
            for j in range(1, n + 1):
                cur = 0
                if grid[i - 1][j - 1] == 0:
                    cur = 1
                P[i][j] = P[i - 1][j] + P[i][j - 1] - P[i - 1][j - 1] + cur

        def get(x, y):
            x = max(min(x, m), 0)
            y = max(min(y, n), 0)
            return P[x][y]

        for i in range(1, m + 1):
            for j in range(1, n + 1):
                if grid[i][j] == 1:
                    continue
                if i + stampHeight - 1 >= m or j + stampWidth - 1 >= n:
                    continue
                SUM = get(i + stampHeight, j + stampWidth) \
                      - get(i - 1, j + stampWidth) - get(i + stampHeight, j - 1) \
                      + get(i - 1, j - 1)
                if SUM == stampWidth * stampHeight:
                    for g in range(i, i + stampHeight):
                        for k in range(j, j + stampWidth):
                            grid[g][k] = 2
        for i in range(m):
            for j in range(n):
                if grid[i][j] == 0:
                    return False
        return True


